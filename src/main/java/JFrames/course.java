    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
          
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        
            Connection  con = DriverManager.getConnection("jdbc:mysql://localhost:3306/collegedata?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "root");
            String code = txtcoursecode.getText();
            String cname=txtcoursename.getText();
            String soy = txtsoy.getText();
            String tsoy = txttsoy.getText();
            String tcredit = txtcredit.getText();
            Statement smt = con.createStatement();
             String quary = "select coursecode from course where coursecode = '"+code+"';";
            ResultSet rs = smt.executeQuery(quary);
            if(rs.next())
            {
                 try
                {
            smt.executeUpdate("update course set coursename='"+cname+"',yearorsem='"+soy+"',totalyearorsem='"+tsoy+"',totalcredit='"+tcredit+"' where coursecode = '"+code+"';");                                                                                                                                                                                                                                                                                                                                  
            JOptionPane.showMessageDialog(this, "Course details updated sucessfully");
            txtcoursecode.setText("");
            txtcoursename.setText("");
            txtsoy.setText("");
            txttsoy.setText("");
            txtcredit.setText("");
            con.close();
            smt.close();
                }
                catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
                
            }
         
          else{
     JOptionPane.showMessageDialog(this, "Please enter right coursecode ");
            }
          } 
          catch (ClassNotFoundException ex) {
            Logger.getLogger(course.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(course.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateMouseClicked

