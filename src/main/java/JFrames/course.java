    private void search1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_search1MouseClicked
        String f = JOptionPane.showInputDialog(this,"Enter a course code.");
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection  con = DriverManager.getConnection("jdbc:mysql://localhost:3306/collegedata?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "root");
            PreparedStatement pst ;
            ResultSet rst;
            pst = con.prepareStatement("select * from course where coursecode=?");
            pst.setString(1,f);
            rst = pst.executeQuery();
            if(rst.next())
            {
                txtcoursecode.setText(rst.getString(1));
                txtcoursename.setText(rst.getString(2));
                txtsoy.setText(rst.getString(3));
                txttsoy.setText(rst.getString(4));
                txtcredit.setText(rst.getString(5));
             }
            else
            {
                 JOptionPane.showMessageDialog(this, "Please enter a course code");
            }
       } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Edit_faculty.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_search1MouseClicked

